				VISI, MISI, MOTTO, PROGRAM UNGGULAN
				<span></span>	
			</h3>
	<h4><b>Visi</b></h4>
	<p> Menjadi BK yang inspiratif, informatif dan menjad <i>professional helping profession</i> dalam rangka membangun dan mengembangkan masa depan peserta didik </p>
	
	<br>
	<br>

	<h4><b>Misi</b></h4>
	<li>Mengedepankan pendidikan karakter  di dalam pendidikan sekolah (intra-ko-ekstrakulikuler)</li>
	<li>Menjadi rujukan siswa dalam menentukan karir setelah lulus (bekerja, melanjutkan, wirausaha)</li>
	<li>Pendampingan peserta didik dalam 4 aspek bidang pribadi, sosial, belajar, dan karir</li>
	<li>Menjadikan pelayanan BK semakin dinamis seiring perkembangan IPTEK</li>

	<br>
	<br>

	<h4><b>Motto</b></h4>
	<p>BK yang cepat, dinamis, dan bersahabar</p>

	<br>
	<br>
	<h4><b>Program Unggulan</b></h4>
	<li>Pelayanan konseling prima didalam dan diluar sekolah</li>
	<li>Pendampingan penuh dalam mencapai tujuan karir (bekerja, melanjutkan, wirausaha)</li>
	<li>Penanaman pendidikan karakter saat siswa masuk sampai keluar lingkungan sekolah</li>
	<li>BK yang dekat dan dinamis dengan siswa</li>
	<li>Penyelesaian masalah secara "on the spot", cepat dan bisa dipertanggungjawabkan</li>


